public class ex03 {
	//Escriure un programa en llenguatge Java on es declari una variable amb un nombre de graus centígrads, i es mostri per pantalla el seu equivalent en graus Fahrenheit.
	public static void main (String[] args) {

		int celsius = 20;
		double Fahrenheit = (celsius*1.8)+32;
		
		System.out.println ("Fahrenheit = " + Fahrenheit);
		System.out.println ("Celsius = " + celsius);

	}
}
